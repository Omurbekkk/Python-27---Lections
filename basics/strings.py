"=========================Форматирование строк=============================="
"самое интересное и нужное"

title = 'Пирог'
price = 35
string = f'Название: {title}, цена: {price}'
print(string)
# Название: Пирог, цена: 35
# f'' это значит что будем сейчас подставлять значение взамен того что в скобках

format1 = 'Название: %s, цена: %s'
print(format1 % ('Яблоко', 80))
# Название: Яблоко, цена: 80

format2 = 'Название: {}, цена: {}'
print(format2.format('Груша', 60))
# Название: Груша, цена: 60

'===========================Методы строк=========================='

# Метод - это функции, которые принадлежат определенному типу данных (обращаемся к ней через точку)

print(dir(str))   # dir показывает какие есть методы для типа данных, здесь str

print('hello'.upper()) # HELLO
print('HELLO'.lower()) # hello

'hello WORLD'.swapcase() # 'HELLO world'   каждую букву

'hello world'.title() # 'Hello World'
'hello world'.capitalize() # 'Hello world'

'hello'.center(11) # '   hello   '
'hello'.center(11, '-') # '---hello---'

'hello'.count('l') # 2
'hello'.count('ll') # 1
'Hello'.count('hello') # 0
'hello'.count('') # 6

'hello world'.split() # ['hello', 'world']
'hello world'.split('o') # ['hell', 'w', 'rld']

' '.join(['hello', 'world'])   # 'hello world'
'%'.join(['hello', 'world'])   # 'hello%world'


'hello world'.find('w') # 6
'hello world'.find('wor') # 6    Выдает индекс первой буквы
'hello world'.find('o') # 4
'hello world'.rfind('o') # 7





