'======================Индексы================'

# это порядковый номер символа в строке (начиная с 0)

'h e l l o'

# 0 1 2 3 4
#      -2 -1

string100 = 'hello world'
print(string100[3])   # l

string100[-1]  # 'd'

# срез - часть строки
string100[6:9] # 'wor'

# string100[:6] = string100[0:6]   то же самое

# с минусом разобраться

string100[6:]   # до конца 

# можно не указывать и начало и конец

string100[0:11:2] # 'hlowrd'
# [0:11:2] то же самое [::2]

print(len(string100))
print(string100[2])

print(string100[-5:-1]) # worl

print(string100[::-1])  # dlrow olleh   СРЕЗ НАОБОРОТ С КОНЦА

